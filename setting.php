    function GetRating($rating) {
        $ratinghtml = '<ul class="list-unstyled d-flex justify-content-center mb-1"><li>';
        for($i = 0; $i < $rating; $i++) {
            $ratinghtml = $ratinghtml + '<i class="text-warning fa fa-star"></i>';
